    path = Path("demo_reports/hhs_attractor_ledger_demo_v1.json")
    if path.exists():
        path.unlink()
    goal = GoalState(
        name="LoShu-Hash72 convergence demo",
        target_token="Hash72",
        target_phase_index=36,
        target_lo_shu_cell=40,
        target_dna_prefix="x",
    )
    receipt = run_attractor(
        ["HHS", "Hash72", "LoShu", "xyzw"],
        goal=goal,
        d_model=72,
        dimensions=4,
        top_k=2,
        max_steps=2,
        ledger_path=path,
    )
    return receipt.to_dict()


